package practice;

public class User {

	/**
	 * IDを保持します
	 */
	private String id;

	/**
	 * 名前を保持します
	 */
	private String name;


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
